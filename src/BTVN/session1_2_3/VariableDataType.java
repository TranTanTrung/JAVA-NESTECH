package BTVN.session1_2_3;

public class VariableDataType {
    public static void main(String[] args) {
        int i = 10; // Khai báo biến i là kiểu int có giá trị là 10
        float f = 20.5f; // Khai báo biến f là kiểu float có giá trị là 20.5
        double d = 20.5; // Khai báo biến d là kiểu double có giá trị là 20.5
        boolean b = true; // Khai báo biến b là kiểu boolean có giá trị là true
        char c = 'a'; // Khai báo biến c là kiểu char có giá trị là a
        String s = "Thanh pho Ho Chi Minh"; // Khai báo biến s là kiểu String có giá trị là Thanh pho Ho Chi Minh
        System.out.println("i = " + i);
        System.out.println("f = " + f);
        System.out.println("d = " + d);
        System.out.println("b = " + b);
        System.out.println("c = " + c);
    }
}
